int MainWindow::listLineType_cb( int i )
{
   static long int cd_line_types[] = 
   {
      CD_CONTINUOUS,
         CD_DASHED,
         CD_DOTTED,
         CD_DASH_DOT,
         CD_DASH_DOT_DOT,
         CD_CUSTOM
   };
   if( _selected_obj.size() > 0)
   {
      std::vector<long int> old_types;
      for( unsigned int o = 0 ; o < _selected_obj.size() ; o ++)
      {
         old_types.push_back( _selected_obj[o]->lineType() ) ;
         _selected_obj[o]->lineType( cd_line_types[i] ) ;
      }    

      ChangeLineTypeCommand* command = new ChangeLineTypeCommand( _selected_obj, cd_line_types[i], old_types );
      CommandStack::insert( command );


      repaintMainCanvas_cb( _iupcanvas, 0, 0 ) ;
   }
   else
      _project->lineType( cd_line_types[i] ) ;


   return 1 ;
};
