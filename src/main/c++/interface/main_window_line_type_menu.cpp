static Ihandle *createImage( int width, int height, unsigned char *pixels, int ncolors, char **colors, char *name )
{
   Ihandle *image  = IupImage ( width, height, (unsigned char*)pixels ) ; 
   for( int c=0 ; c<ncolors ; c++ )
   {
      char color_index[64] ;
      sprintf( color_index, "%d", c ) ;
      IupSetAttribute( image, color_index, colors[c] ) ;        
   }
   IupSetHandle( name, image ) ;

   return image ;
}

