int MainWindow::listLineSize_cb( int i )
{
   if( _selected_obj.size() > 0)   
   {
      std::vector< long int > old_sizes;
      for( unsigned int o = 0 ; o < _selected_obj.size() ; o ++)
      {
         old_sizes.push_back( _selected_obj[o]->lineSize() ) ;
         _selected_obj[o]->lineSize( i+1 ) ;         
      }
      repaintMainCanvas_cb( _iupcanvas, 0, 0 ) ;

      ChangeLineSizeCommand* command = new ChangeLineSizeCommand( _selected_obj, i+1, old_sizes );
      CommandStack::insert( command );
   }
   else 
      _project->lineSize( i+1 ) ;

   return 1 ;
}



